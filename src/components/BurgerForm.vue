<script>

const url = 'http://localhost:3000/ingredients';

export default {
  name: "BurgerForm",
  data() {
    return {
      breads: null,
      meats: null,
      optional_data: null,
      name_client: null,
      bread: null,
      meat: null,
      optionals: [],
      status: 'Solicitado',
      msg: null,
    }
  },
  methods: {
    async getIngredients() {
      const req = await fetch(url);
      const data = await req.json();

      this.breads = data.breads;
      this.meats = data.meats;
      this.optional_data = data.optionals;
    }
  },
  mounted() {
    this.getIngredients();
  }
}
</script>

